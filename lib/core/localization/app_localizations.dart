  String t(String key) {
    return _localizedValues[locale.languageCode]?[key] ??
        _extendedLocalizedValues[locale.languageCode]?[key] ??
        _localizedValues['en']![key] ??
        _extendedLocalizedValues['en']?[key] ??
        key;
  }

  String tf(String key, [Map<String, Object?> params = const {}]) {
    var text = t(key);
    for (final entry in params.entries) {
      text = text.replaceAll('{${entry.key}}', '${entry.value ?? ''}');
    }
    return text;
  }

  String upper(String key) => upperText(t(key));

  String upperText(String text) {
    if (locale.languageCode == 'tr') {
      // Turkish-specific casing for dotted/dotless i before generic uppercasing.
      return text.replaceAll('i', 'İ').replaceAll('ı', 'I').toUpperCase();
    }
    return text.toUpperCase();
  }

  static String languageLabel(String code) {
    switch (code) {
      case 'tr':
        return 'Türkçe';
      case 'de':
        return 'Deutsch';
      case 'ru':
        return 'Русский';
      case 'fr':
        return 'Français';
      case 'es':
        return 'Español';
      default:
        return 'English';
    }
  }
